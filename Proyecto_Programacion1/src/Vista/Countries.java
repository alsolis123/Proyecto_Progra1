
package Vista;


public class Countries {
    private String url;
    private int count;
    private int sCode;

    public Countries(String url, int count, int sCode) {
        this.url = "";
        this.count = 0;
        this.sCode = 0;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getsCode() {
        return sCode;
    }

    public void setsCode(int sCode) {
        this.sCode = sCode;
    }
    
    
    
    
    
    
}
